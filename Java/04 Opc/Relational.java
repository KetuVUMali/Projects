public class Relational {
    public static void main(String[] args) {
        int a=10,b=20;

        boolean res;

        res= a<b;
        System.out.println(res);

        res= a>b;
        System.out.println(res);

        res= a==b;
        System.out.println(res);

        res= a>=b;
        System.out.println(res);

        res=a<=b;
        System.out.println(res);

        res= a!=b;
        System.out.println(res);

        res= a==b;
        System.out.println(res);

    }
}

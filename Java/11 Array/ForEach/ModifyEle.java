
public class ModifyEle {
    public static void main(String[] args)
     {
        int[] numbers = {1, 2, 3, 4, 5};

        System.out.println("Double of each number:");
        for (int number : numbers) {
            int doubleValue = number * 2;
            System.out.println(doubleValue);
        }
    }
}
